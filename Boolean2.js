/* Boolean2

Дано целое число A. Проверить истинность высказывания: «Число A является 
нечетным» */

// ввід значення змінної А через модальне вікно
let A = parseInt(prompt("Введіть число А"))

// перевірка числа на непарність
let isOdd = A % 2 > 0

// вивід результата через модальне вікно
alert(isOdd)